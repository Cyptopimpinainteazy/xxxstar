#!/usr/bin/env node
/**
 * scripts/generate-chain-registry.js
 *
 * Usage: node scripts/generate-chain-registry.js path/to/chains.json
 *
 * Input: JSON array of ChainDescriptor objects (matching packages/blockchain-connector/src/types/index.ts)
 * Output: writes `packages/blockchain-connector/src/chains/generated/index.ts`
 */

const fs = require('fs');
const path = require('path');

if (process.argv.length < 3) {
  console.error('Usage: node scripts/generate-chain-registry.js path/to/chains.json');
  process.exit(2);
}

const input = process.argv[2];
const outFile = path.resolve(__dirname, '..', 'packages', 'blockchain-connector', 'src', 'chains', 'generated', 'index.ts');

try {
  const raw = fs.readFileSync(input, 'utf8');
  const arr = JSON.parse(raw);
  if (!Array.isArray(arr)) throw new Error('Input JSON must be an array');

  const header = `/**
 * GENERATED FILE — DO NOT EDIT BY HAND
 * Generated by scripts/generate-chain-registry.js
 */\n\n`;

  const content = header + "import type { ChainDescriptor } from \"../../types\";\n\n" +
    `export const GENERATED_CHAIN_REGISTRY: ChainDescriptor[] = ${JSON.stringify(arr, null, 2)};\n`;

  fs.writeFileSync(outFile, content, 'utf8');
  // Also write a companion JSON file for validation and tooling
  const jsonOut = outFile.replace(/index\.ts$/, 'chains.json');
  fs.writeFileSync(jsonOut, JSON.stringify(arr, null, 2), 'utf8');

  console.log('Wrote', outFile, 'and', jsonOut);
} catch (err) {
  console.error('Error generating registry:', err.message);
  process.exit(1);
}
