#include <stdio.h>
#include <stdint.h>
#include <string.h>

/* Host API provided by ed25519_batch.cu */
extern "C" int ed25519_verify_batch_host(
    const unsigned char *entries,
    int count,
    unsigned char *results
);

int main() {
    /* Deterministic test vector (Ed25519 signature over SHA256(message)). */
    const unsigned char msg[32] = {
        0x74, 0x65, 0x73, 0x74, 0x2d, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65, 0x2d, 0x33, 0x32, 0x2d,
        0x62, 0x79, 0x74, 0x65, 0x2d, 0x6c, 0x65, 0x6e,
        0x67, 0x74, 0x68, 0x2d, 0x31, 0x32, 0x33, 0x34,
    };

    const unsigned char pubkey[32] = {
        0x79, 0xe4, 0x3a, 0x59, 0x01, 0x51, 0x6a, 0x95,
        0x2f, 0x7d, 0x2e, 0x7b, 0x9e, 0x9a, 0x63, 0xcc,
        0xb9, 0x52, 0xa6, 0x0d, 0x80, 0x94, 0xa2, 0x01,
        0xd1, 0xe8, 0x7a, 0xc3, 0xf4, 0x38, 0x16, 0x70,
    };

    const unsigned char sig[64] = {
        0xe5, 0xb4, 0xba, 0xbe, 0x71, 0x84, 0x4f, 0x3a,
        0xb6, 0xcc, 0x12, 0x34, 0x5b, 0x3c, 0x3c, 0xbc,
        0x25, 0xa3, 0xd6, 0xdb, 0xcc, 0x8a, 0xf4, 0xb1,
        0x9e, 0xd7, 0x22, 0x6a, 0x8b, 0x28, 0xb3, 0x6d,
        0x44, 0x4e, 0x97, 0xfe, 0x12, 0xce, 0xb6, 0x29,
        0xfb, 0x81, 0xd8, 0x16, 0xcd, 0xdf, 0xd8, 0xa3,
        0x6b, 0xcd, 0x6f, 0xaa, 0xfa, 0xb9, 0xbc, 0xe1,
        0x47, 0xd9, 0x0b, 0x0a, 0x16, 0xa2, 0x9e, 0x04,
    };

    unsigned char entry[128];
    memcpy(entry + 0, sig, 64);
    memcpy(entry + 64, pubkey, 32);
    memcpy(entry + 96, msg, 32);

    unsigned char out[1] = {0};

    int rc = ed25519_verify_batch_host(entry, 1, out);
    if (rc != 0) {
        fprintf(stderr, "ed25519_verify_batch_host returned %d\n", rc);
        return 1;
    }
    if (out[0] != 1) {
        fprintf(stderr, "ed25519_verify_batch_host returned invalid (expected 1, got %d)\n", (int)out[0]);
        return 1;
    }

    printf("ed25519 kernel self-test passed\n");
    return 0;
}
